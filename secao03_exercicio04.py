''' Seção 03 - Exercício 4
    
    Faça um algoritmo que peça dois números e imprima a soma.
'''
#Entrada
num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

#Processamento
soma = num1 + num2

#Saída
print("A soma dos números é: {0}".format(soma))